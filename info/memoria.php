                        <h2>Memoria a corto plazo (MCP)</h2>
                        <p>También llamada memoria activa o primaria, es la que permite retener una cantidad limitada de información durante un periodo corto de tiempo, alrededor de veinte segundos, con el fin de almacenarla posteriormente en la memoria a largo plazo.. Transcurrido este tiempo, el recuerdo se olvida, desaparece y es sustituido por otros nuevos. 
                            La memoria a corto plazo tiene dos propiedades principales: una capacidad limitada y una duración infinita.
                            </p>
                        
