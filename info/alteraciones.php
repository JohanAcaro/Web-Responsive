        <div class="container">
            <div class="row">
                <div class="col-lg-12">
                    <h2>Tipos de amnesia</h2>
                </div> <!-- end of col -->
            </div> <!-- end of row -->
            <div class="row">
                    <div class="col-lg-6">
                        <ul class="list-unstyled li-space-lg first">
                            <li class="media">
                                <i class="bullet">1</i>
                                <div class="media-body">
                                    <h4>Amnesia retrógrada: </h4>
                                    <p>No consigue recordar aquellos sucesos que ocurrieron antes de desarrollar este trastorno.</p>
                                </div>
                            </li>
                            <li class="media">
                                <i class="bullet">2</i>
                                <div class="media-body">
                                    <h4>Amnesia anterógrada: </h4>
                                    <p>La información nueva que se recibe no se guarda en la memoria a largo plazo, entonces, una vez que desaparece de la memoria a corto plazo es imposible recordarla.</p>
                                </div>
                            </li>
                            <li class="media">
                                <i class="bullet">3</i>
                                <div class="media-body">
                                    <h4>Amnesia global: </h4>
                                    <p>Consiste en la pérdida total de la memoria, aunque se mantienen aquellos recuerdos que están relacionados con la propia identidad.</p>
                                </div>
                            </li>
                            <li class="media">
                                <i class="bullet">4</i>
                                <div class="media-body">
                                    <h4>Amnesia global transitoria: </h4>
                                    <p>Al igual que la amnesia global recuerda todo aquello que esté relacionado con su identidad, y en este caso también recordará cosas del pasado. Tendrá problemas para recordar los hechos que hayan ocurrido hace tan solo unos minutos.</p>
                                </div>
                            </li>
                        </ul>
                    </div> <!-- end of col -->
                    <div class="col-lg-6">
                        <ul class="list-unstyled li-space-lg second">
                            <li class="media">
                                <i class="bullet">5</i>
                                <div class="media-body">
                                    <h4>Amnesia disociativa: </h4>
                                    <p>No permite recordar los sucesos que están relacionados con experiencias traumáticas.</p>
                                </div>
                            </li>
                            <li class="media">
                                <i class="bullet">6</i>
                                <div class="media-body">
                                    <h4>Amnesia lacunar: </h4>
                                    <p>Es imposible recordar lo que  pasó en un periodo de tiempo determinado en el que no hubo ningún tipo de estrés. Es conocido como “lagunas en blanco”.</p>
                                </div>
                            </li>
                            <li class="media">
                                <i class="bullet">7</i>
                                <div class="media-body">
                                    <h4>Amnesia postraumática: </h4>
                                    <p>Se produce por un golpe en la cabeza o por un traumatismo craneoencefálico. Es transitoria y afecta a los recuerdos del pasado.</p>
                                </div>
                            </li>
                            <li class="media">
                                <i class="bullet">8</i>
                                <div class="media-body">
                                    <h4>Fuga disociativa: </h4>
                                    <p>este tipo se observa más frecuentemente en aquellas personas que sufren demencia. Las personas se dan cuenta de que han llegado a un lugar pero no saben cómo.
                                </div>
                            </li>
                        </ul>
