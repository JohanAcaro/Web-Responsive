                    <h2>Tipos de demencia</h2>
                </div> <!-- end of col -->
            </div> <!-- end of row -->
            <div class="row">
                    <div class="col-lg-6">
                        <ul class="list-unstyled li-space-lg first">
                            <li class="media">
                                <i class="bullet">1</i>
                                <div class="media-body">
                                    <h4>Demencia vascular: </h4>
                                    <p>Este tipo de demencia es causada por el daño de los vasos sanguíneos que llevan la sangre al cerebro. Puede afectar al cerebro de diferentes maneras, como dañando sus fibras o incluso puede causar accidentes cerebrovasculares. Sus síntomas más comunes son la dificultad para resolver problemas, lentitud del pensamiento y pérdida de la organización y el pensamiento.</p>
                                </div>
                            </li>
                            <li class="media">
                                <i class="bullet">2</i>
                                <div class="media-body">
                                    <h4>Demencia frontotemporal: </h4>
                                    <p>Se caracteriza por la ruptura de las células nerviosas y sus conexiones en los lóbulos frontal y temporal. Como estas áreas están asociadas a la personalidad, la conducta y el lenguaje, sus síntomas se ven reflejados en el movimiento, el lenguaje, la personalidad y el pensamiento. </p>
                                </div>
                            </li>
                            
                            
                        </ul>
                    </div> <!-- end of col -->
                    <div class="col-lg-6">
                        <ul class="list-unstyled li-space-lg second">
                            <li class="media">
                                <i class="bullet">3</i>
                                <div class="media-body">
                                    <h4>Demencia con cuerpos de Lewy: </h4>
                                    <p>Este es uno de los tipos más comunes de demencia progresiva. Los signos más habituales son: actúan físicamente los sueños durante el sueño, sufren alucinaciones visuales, presentan problemas con la atención y el concentramiento. Además, también puede sufrir de temblores, movimientos descoordinados y rigidez.</p>
                                </div>
                            </li>
                            <li class="media">
                                <i class="bullet">4</i>
                                <div class="media-body">
                                    <h4>Demencia mixta: </h4>
                                    <p>Es una combinación de varias causas de la demencia (demencia vascular, de Lewy, alzheimer…). Se ha observado que este tipo de demencias se ha dado en personas de 80 años o más y aún se sigue investigando el hecho de sufrir demencia mixta, sus síntomas y tratamientos.</p>
                                </div>
                            </li>
                            
                        </ul>
