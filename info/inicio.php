            <p>El hipocampo es la parte cerebral que está más relacionada con la memoria, también se cree que tiene un papel continuo en los recuerdos, ya que mantiene los detalles precisos y evita que se confundan unos recuerdos con otros. 
            En el hemisferio derecho se  guardan los significados de las palabras, en los lóbulos frontales se organizan la percepción y el pensamiento y en la corteza prefrontal se pueden almacenar los recuerdos.
