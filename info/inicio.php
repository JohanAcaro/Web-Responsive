            <p>La memoria se puede definir como la capacidad del cerebro de retener información y recuperarla voluntariamente. Es esencial para la creación y el desarrollo de nuestra personalidad. Por tanto, es una de las funciones cognitivas más esenciales en la vida de las personas.</p>
